DROP TABLE IF EXISTS LIGHT_AFFECT.PRODUCT_PHOTO;
DROP TABLE IF EXISTS LIGHT_AFFECT.PRODUCT;
DROP TABLE IF EXISTS LIGHT_AFFECT.CHOICE;
DROP TABLE IF EXISTS LIGHT_AFFECT.QUESTION;
DROP TABLE IF EXISTS LIGHT_AFFECT.CONTACT;


CREATE TABLE LIGHT_AFFECT.QUESTION (
  ID    INT AUTO_INCREMENT NOT NULL,
  NAME  VARCHAR(255),
  TOPIC VARCHAR(255),
  PRIMARY KEY (`ID`)
);


CREATE TABLE LIGHT_AFFECT.CHOICE (
  ID              INT AUTO_INCREMENT NOT NULL,
  NAME            VARCHAR(255),
  QUESTIONID      INT,
  LAST_SELECTED   INT,
  NEXT_QUESTIONID INT,
  PRIMARY KEY (`ID`),
  CONSTRAINT la_question FOREIGN KEY (QUESTIONID)
  REFERENCES QUESTION (ID)
);


CREATE TABLE LIGHT_AFFECT.PRODUCT (
  ID    INT,
  PRICE VARCHAR(255),
  CHOICE_ID  INT,
  PRIMARY KEY (`ID`),
  CONSTRAINT la_productType FOREIGN KEY (CHOICE_ID)
  REFERENCES CHOICE (ID)
);

CREATE TABLE LIGHT_AFFECT.PRODUCT_PHOTO (
  ID         INT,
  URL        VARCHAR(255),
  PHOTO      BLOB,
  PRODUCT_ID INT,
  TARGET_URL VARCHAR(255),
  PRIMARY KEY (`ID`),
  CONSTRAINT la_product_photo FOREIGN KEY (PRODUCT_ID)
  REFERENCES PRODUCT (ID)

);


CREATE TABLE LIGHT_AFFECT.CONTACT (
  ID      INT AUTO_INCREMENT NOT NULL,
  PLACE   VARCHAR(255),
  PHONE   VARCHAR(255),
  ADDRESS VARCHAR(255),
  PRIMARY KEY (`ID`)
);

